const defaultHoursByWindow = (windows: ExperienceNode["operatingWindows"]): OperatingHours => {
  if (windows.includes("night")) {
    return { openHour: 18, closeHour: 2 };
  }

  if (windows.includes("blue-hour")) {
    return { openHour: 16, closeHour: 23 };
  }

  if (windows.includes("morning") && !windows.includes("afternoon")) {
    return { openHour: 8, closeHour: 13 };
  }

  return { openHour: 10, closeHour: 20 };
};

